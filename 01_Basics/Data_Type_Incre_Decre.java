public class Data_Type_Incre_Decre 
{
    public static void main(String[] args) 
    {

        // Data type Expressions

        byte x = 5;
        int y = 6;
        short z = 8;
        int a = y + z;
        float b = 6.54f + x;

        System.out.println(a);
        System.out.println(b);

        int i = 10;

        //  Increment  
        // Post
        System.out.println(i++);
        System.out.println(i);
        //Pre
        System.out.println(++i);
        System.out.println(i);

        int j = 12;

        //Decrement
        //post
        System.out.println(j--);
        System.out.println(j);
        //Pre
        System.out.println(--j);
        System.out.println(j);
        
    }
}

