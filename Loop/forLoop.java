public class forLoop 
{
    public static void main(String[] args) 
    {
        System.out.println("First 10 Odd Numbers: ");
        for(int i = 0;i <= 10;i++)
        {
            System.out.println(2*i+1);
        }

        System.out.println("Reverse Numbers form 5.");
        for(int j = 5;j >=0;j--)
        {
            System.out.println(j);
        }
    }
}
